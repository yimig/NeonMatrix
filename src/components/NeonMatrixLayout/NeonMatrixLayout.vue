    convertText(text){
      if(this.$data.dataset.length==0){
        for (let i = 0; i < this.$data.font_dict[0].length; i++) {
          this.$data.dataset.push([])
        }
      }
      text.split('').forEach((item)=>{
        this.mergeMatrix(this.$data.dataset,this.$data.font_dict[item]!=null?this.$data.font_dict[item]:this.$data.font_dict['?'])
      })

    },
    mergeMatrix:function (original,append) {
      for (let i = 0; i < original.length; i++) {
        for (let j = 0; j < append[i].length; j++) {
          original[i].push(append[i][j])
        }
      }
    },
    initMatrix:function(event){
      let layout=d3.select('#neon_matrix').insert('svg').attr('height',this.$data.height).attr('width',this.$data.width)
      let layout_matrix=[]
      this.$data.block_row_num=this.$data.dataset.length
      this.$data.block_column_num=this.$data.dataset[0].length
      let avg_height=Number.parseInt(this.$data.height/this.$data.block_row_num)
      let avg_width=Number.parseInt(this.$data.width/this.$data.block_column_num)
      let rect_length_max=avg_height>avg_width?avg_width:avg_height
      let rect_length=rect_length_max-this.$data.gap
      console.log("block_row_num/block_column_num: "+this.$data.block_row_num+"/"+this.$data.block_column_num)
      console.log("avg_height/avg_width: "+avg_height+'/'+avg_width)
      console.log("rect_length_max:"+rect_length_max+"; rect_length:"+rect_length)
      for(let i=0;i<this.$data.block_row_num;i++){
        let matrix_column=[]
        for(let j=0;j<this.$data.block_column_num;j++){
          matrix_column.push(this.initPattern(layout,j,i,rect_length))
        }
        layout_matrix.push(matrix_column)
      }
      this.$data.layout_matrix=layout_matrix
    },
    initPattern:function (layout,column,row,rect_length) {
      return layout.insert('rect').attr('height',rect_length).attr('width',rect_length)
          .attr('y',row*(rect_length+this.$data.gap)).attr('x',column*(rect_length+this.$data.gap))
          .attr('rx','4').attr('fill','#1f2d3d')
    },
    refurbishFrame:function(){
      let isFlash=this.$data.isFlash
      this.clearFrame(isFlash)
      // this.addIndex()
      for(let i=0;i<this.$data.block_row_num;i++){
        for(let j=0;j<this.$data.block_column_num;j++){
          if(this.$data.dataset[i][j]==1){
            if(isFlash)this.$data.layout_matrix[i][j].transition().delay(this.$data.delay+Mock.Random.integer(0,this.$data.delay)).attr('fill','orange')
            else this.$data.layout_matrix[i][j].attr('fill','orange')
          }
        }
      }
    },
    clearFrame:function(isFlash){
      for(let i=0;i<this.$data.block_row_num;i++){
        for(let j=0;j<this.$data.block_column_num;j++){
          if(isFlash)this.$data.layout_matrix[i][j].transition().delay(Mock.Random.integer(0,this.$data.delay)).attr('fill','#1f2d3d')
          else this.$data.layout_matrix[i][j].attr('fill','#1f2d3d')
        }
      }
    },
    addIndex:function(event){
      this.$data.now_index=this.$data.now_index==this.$data.dataset.length-1?0:this.$data.now_index+1
    }
